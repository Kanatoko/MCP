package net.jumperz.util;

public class MCount
{
private int value;
//------------------------------------------------------------------------------------------
public MCount()
{
value = 1;
}
// --------------------------------------------------------------------------------
public MCount( int i )
{
value = i;
}
// --------------------------------------------------------------------------------
public void inc( int i )
{
value += i;
}
//------------------------------------------------------------------------------------------
public void inc()
{
value ++;
}
//------------------------------------------------------------------------------------------
public int getValue()
{
return value;
}
//------------------------------------------------------------------------------------------
public void dec( int i )
{
value -= i;
}
//------------------------------------------------------------------------------------------
public void dec()
{
value --;
}
// --------------------------------------------------------------------------------
public void setValue( int i )
{
value = i;
}
//------------------------------------------------------------------------------------------
public String toString()
{
return String.valueOf( value );
}
//------------------------------------------------------------------------------------------
}